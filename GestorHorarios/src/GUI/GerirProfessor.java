    public GerirProfessor(javax.swing.JFrame frameInicial,javax.swing.JFrame frame,Dados dados) {
        initComponents();
        
        List<Professor> listaH=dados.getProfessores();
        for(int i=0;i<listaH.size();i++){
            jComboBox1.addItem(dados.getProfessoresNome(listaH.get(i)));
            jComboBox1.setSelectedItem(null);
        }  
        jComboBox1.addItemListener(this);
        
        this.frameInicial=frameInicial;
        this.frame=frame;
        this.dados=dados;
        
        centreWindow(frame);
    }
    
    public void itemStateChanged(ItemEvent e) {
        if ((e.getStateChange() == ItemEvent.SELECTED)) {
            List<Professor> listaH=dados.getProfessores();
            int selection = jComboBox1.getSelectedIndex();
            jTextField3.setText(listaH.get(selection).getNome());
            jTextField4.setText(listaH.get(selection).getIdade()+"");
            jTextField2.setText(listaH.get(selection).getGrauAcadémico().toString());
        }
    }
    
    
    public static void centreWindow(Window frame) {
        Dimension dimension = Toolkit.getDefaultToolkit().getScreenSize();
        int x = (int) ((dimension.getWidth() - frame.getWidth()) / 2);
        int y = (int) ((dimension.getHeight() - frame.getHeight()) / 2);
        frame.setLocation(x, y);
    }

