    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jComboBox1.getItemCount()>0 && jComboBox1.getSelectedIndex()!=-1){
            List<Professor> listaH=dados.getProfessores();
            listaH.remove(jComboBox1.getSelectedIndex());
            frameInicial.setVisible(true);
            frame.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Nao existem professores para remover", "Remover Professores", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

